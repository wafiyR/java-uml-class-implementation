package bean;

public class Department
{
	public int department;
	public String name;
	
	public Department()
	{
	}
	
	public void setDepartment(int department)
	{
		this.department = department;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public int getDepartment()
	{
		return department;
	}
	
	public String getName()
	{
		return name;
	}
}